#include<stdio.h>
#include <string.h>
#include <stdlib.h>

//左移运算符 
void test(){
	int num=10;
	//0000 1010
	//0010 1000
	//等价于乘以2的2次方 
	//左移用0填充 
	printf("左移运算符:%d",num<<2);
}

void test01(){
	int num=10;
	//等价于除以2的2次方  （并不是这样的） 
	//0000 1010
	//0000 0010 
	printf("右移运算符：%d",num>>2);
	
	//注意点
	//无符号数
	//10001010 >> 2
	//所有系统结果值 
	//00100010 
	
	//有符号数
	//10001010 >> 2
	//某些系统 ，用0填充 
	//00100010
	//另一些些系统 ，用1填充  
	//11100010 
} 

int main(){
	test01();
} 
